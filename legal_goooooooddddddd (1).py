def get_rank_scores(embeddings):
    # Graph-based ranking using Semantic Cosine Similarity
    sim_matrix = cosine_similarity(embeddings)
    nx_graph = nx.from_numpy_array(sim_matrix)
    scores = nx.pagerank(nx_graph, max_iter=200)
    return np.array([scores[i] for i in range(len(embeddings))])

def build_candidate_pool(sentences, embeddings):
    topic_labels = lda_clustering(sentences)
    candidate_indices = set()

    # 1. Local importance (within clusters)
    for topic in set(topic_labels):
        indices = [i for i, label in enumerate(topic_labels) if label == topic]
        if indices:
            cluster_embeds = embeddings[indices]
            local_scores = get_rank_scores(cluster_embeds)
            best_in_cluster = indices[np.argmax(local_scores)]
            candidate_indices.add(best_in_cluster)

    # 2. Global importance (LexRank across whole doc)
    global_scores = get_rank_scores(embeddings)
    top_global = np.argsort(global_scores)[-5:] # Top 5 global sentences
    for idx in top_global:
        candidate_indices.add(idx)

    return list(candidate_indices)

def knapsack_selection(sentences, candidate_indices, embeddings, max_words):
    # Values = Similarity to the overall document mean vector (Semantic Value)
    doc_embedding = embeddings.mean(axis=0).reshape(1, -1)
    cand_embeddings = embeddings[candidate_indices]
    values = cosine_similarity(cand_embeddings, doc_embedding).flatten()

    weights = [len(sentences[i].split()) for i in candidate_indices]
    n = len(candidate_indices)

    dp = np.zeros((n + 1, max_words + 1))
    for i in range(1, n + 1):
        for w in range(max_words + 1):
            if weights[i-1] <= w:
                dp[i][w] = max(values[i-1] + dp[i-1][w-weights[i-1]], dp[i-1][w])
            else:
                dp[i][w] = dp[i-1][w]

    selected_sentences = []
    w = max_words
    for i in range(n, 0, -1):
        if dp[i][w] != dp[i-1][w]:
            selected_sentences.append(sentences[candidate_indices[i-1]])
            w -= weights[i-1]

    return " ".join(selected_sentences[::-1])

