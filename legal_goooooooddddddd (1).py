def knapsack(sentences, values, max_words):

    n = len(
        sentences)
    weights = [len(s.split()) for s in sentences]

    dp = [[0]*(max_words+1) for _ in range(n+1)]

    for i in range(1, n+1):
        for w in range(max_words+1):
            if weights[i-1] <= w:
                dp[i][w] = max(values[i-1] + dp[i-1][w-weights[i-1]],
                               dp[i-1][w])
            else:
                dp[i][w] = dp[i-1][w]

    selected = []
    w = max_words

    for i in range(n, 0, -1):
        if dp[i][w] != dp[i-1][w]:
            selected.append(sentences[i-1])
            w -= weights[i-1]

    selected.reverse()

    return " ".join(selected)

